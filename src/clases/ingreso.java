package clases;

public class ingreso {

	int id_ingreso;
	String tipo_ingreso;
	Double cantidad_ingreso;
	String descripcion_ingreso;
	String fecha_ingreso;

	public int getId_ingreso() {
		return id_ingreso;
	}

	public void setId_ingreso(int id_ingreso) {
		this.id_ingreso = id_ingreso;
	}

	public String getTipo_ingreso() {
		return tipo_ingreso;
	}

	public void setTipo_ingreso(String tipo_ingreso) {
		this.tipo_ingreso = tipo_ingreso;
	}

	public Double getCantidad_ingreso() {
		return cantidad_ingreso;
	}

	public void setCantidad_ingreso(Double cantidad_ingreso) {
		this.cantidad_ingreso = cantidad_ingreso;
	}

	public String getDescripcion_ingreso() {
		return descripcion_ingreso;
	}

	public void setDescripcion_ingreso(String descripcion_ingreso) {
		this.descripcion_ingreso = descripcion_ingreso;
	}

	public String getFecha_ingreso() {
		return fecha_ingreso;
	}

	public void setFecha_ingreso(String fecha_ingreso) {
		this.fecha_ingreso = fecha_ingreso;
	}

}
