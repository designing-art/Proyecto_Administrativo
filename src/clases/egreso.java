package clases;

public class egreso {

	int id_egreso;
	String tipo_egreso;
	Double cantidad_egreso;
	String descripcion_egreso;
	String fecha_egreso;

	public int getId_egreso() {
		return id_egreso;
	}

	public void setId_egreso(int id_egreso) {
		this.id_egreso = id_egreso;
	}

	public String getTipo_egreso() {
		return tipo_egreso;
	}

	public void setTipo_egreso(String tipo_egreso) {
		this.tipo_egreso = tipo_egreso;
	}

	public Double getCantidad_egreso() {
		return cantidad_egreso;
	}

	public void setCantidad_egreso(Double cantidad_egreso) {
		this.cantidad_egreso = cantidad_egreso;
	}

	public String getDescripcion_egreso() {
		return descripcion_egreso;
	}

	public void setDescripcion_egreso(String descripcion_egreso) {
		this.descripcion_egreso = descripcion_egreso;
	}

	public String getFecha_egreso() {
		return fecha_egreso;
	}

	public void setFecha_egreso(String fecha_egreso) {
		this.fecha_egreso = fecha_egreso;
	}

}
