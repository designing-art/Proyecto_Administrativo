package clases;

public class bonificacion {

	int id_bonificacion;
	String tipo_bonificacion;
	String observacion_bonificacion;
	String identidad_empleado_bonificacion;
	double cantidad_bonificacion;
	String fecha_bonificacion;

	public int getId_bonificacion() {
		return id_bonificacion;
	}

	public void setId_bonificacion(int id_bonificacion) {
		this.id_bonificacion = id_bonificacion;
	}

	public String getTipo_bonificacion() {
		return tipo_bonificacion;
	}

	public void setTipo_bonificacion(String tipo_bonificacion) {
		this.tipo_bonificacion = tipo_bonificacion;
	}

	public String getObservacion_bonificacion() {
		return observacion_bonificacion;
	}

	public void setObservacion_bonificacion(String observacion_bonificacion) {
		this.observacion_bonificacion = observacion_bonificacion;
	}

	public String getIdentidad_empleado_bonificacion() {
		return identidad_empleado_bonificacion;
	}

	public void setIdentidad_empleado_bonificacion(String identidad_empleado_bonificacion) {
		this.identidad_empleado_bonificacion = identidad_empleado_bonificacion;
	}

	public double getCantidad_bonificacion() {
		return cantidad_bonificacion;
	}

	public void setCantidad_bonificacion(double cantidad_bonificacion) {
		this.cantidad_bonificacion = cantidad_bonificacion;
	}

	public String getFecha_bonificacion() {
		return fecha_bonificacion;
	}

	public void setFecha_bonificacion(String fecha_bonificacion) {
		this.fecha_bonificacion = fecha_bonificacion;
	}

}
