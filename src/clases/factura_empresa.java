package clases;

public class factura_empresa {
	int id_factura;
	String fecha_factura;
	String compra_factura;
	Double precio_factura;
	String cantidad_factura;
	String descripcion_factura;
	String foto_factura;

	public int getId_factura() {
		return id_factura;
	}

	public void setId_factura(int id_factura) {
		this.id_factura = id_factura;
	}

	public String getFecha_factura() {
		return fecha_factura;
	}

	public void setFecha_factura(String fecha_factura) {
		this.fecha_factura = fecha_factura;
	}

	public String getCompra_factura() {
		return compra_factura;
	}

	public void setCompra_factura(String compra_factura) {
		this.compra_factura = compra_factura;
	}

	public Double getPrecio_factura() {
		return precio_factura;
	}

	public void setPrecio_factura(Double precio_factura) {
		this.precio_factura = precio_factura;
	}

	public String getCantidad_factura() {
		return cantidad_factura;
	}

	public void setCantidad_factura(String cantidad_factura) {
		this.cantidad_factura = cantidad_factura;
	}

	public String getDescripcion_factura() {
		return descripcion_factura;
	}

	public void setDescripcion_factura(String descripcion_factura) {
		this.descripcion_factura = descripcion_factura;
	}

	public String getFoto_factura() {
		return foto_factura;
	}

	public void setFoto_factura(String foto_factura) {
		this.foto_factura = foto_factura;
	}

}
