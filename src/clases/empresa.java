	public void setCorreo_empresa(String correo_empresa) {
		this.correo_empresa = correo_empresa;
	}
	public String getCuenta_bancaria_empresa() {
		return cuenta_bancaria_empresa;
	}
	public void setCuenta_bancaria_empresa(String cuenta_bancaria_empresa) {
		this.cuenta_bancaria_empresa = cuenta_bancaria_empresa;
	}
	
	

