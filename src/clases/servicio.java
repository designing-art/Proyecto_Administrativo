package clases;

public class servicio {
	int id_servicio;
	String tipo_servicio;
	String tiempo_servicio;
	Double precio_servicio;
	String descripcion_servicio;
	String producto_servicio;

	public int getId_servicio() {
		return id_servicio;
	}

	public void setId_servicio(int id_servicio) {
		this.id_servicio = id_servicio;
	}

	public String getTipo_servicio() {
		return tipo_servicio;
	}

	public void setTipo_servicio(String tipo_servicio) {
		this.tipo_servicio = tipo_servicio;
	}

	public String getTiempo_servicio() {
		return tiempo_servicio;
	}

	public void setTiempo_servicio(String tiempo_servicio) {
		this.tiempo_servicio = tiempo_servicio;
	}

	public Double getPrecio_servicio() {
		return precio_servicio;
	}

	public void setPrecio_servicio(Double precio_servicio) {
		this.precio_servicio = precio_servicio;
	}

	public String getDescripcion_servicio() {
		return descripcion_servicio;
	}

	public void setDescripcion_servicio(String descripcion_servicio) {
		this.descripcion_servicio = descripcion_servicio;
	}

	public String getProducto_servicio() {
		return producto_servicio;
	}

	public void setProducto_servicio(String producto_servicio) {
		this.producto_servicio = producto_servicio;
	}

}
