package clases;

public class cliente {

	int id_cargo;
	String nombre_cargo;
	String area_cargo;
	Double valor_hora_extra_cargo;
	Double sueldo_cargo;
	String funciones_cargo;

	public int getId_cargo() {
		return id_cargo;
	}

	public void setId_cargo(int id_cargo) {
		this.id_cargo = id_cargo;
	}

	public String getNombre_cargo() {
		return nombre_cargo;
	}

	public void setNombre_cargo(String nombre_cargo) {
		this.nombre_cargo = nombre_cargo;
	}

	public String getArea_cargo() {
		return area_cargo;
	}

	public void setArea_cargo(String area_cargo) {
		this.area_cargo = area_cargo;
	}

	public Double getValor_hora_extra_cargo() {
		return valor_hora_extra_cargo;
	}

	public void setValor_hora_extra_cargo(Double valor_hora_extra_cargo) {
		this.valor_hora_extra_cargo = valor_hora_extra_cargo;
	}

	public Double getSueldo_cargo() {
		return sueldo_cargo;
	}

	public void setSueldo_cargo(Double sueldo_cargo) {
		this.sueldo_cargo = sueldo_cargo;
	}

	public String getFunciones_cargo() {
		return funciones_cargo;
	}

	public void setFunciones_cargo(String funciones_cargo) {
		this.funciones_cargo = funciones_cargo;
	}

}
