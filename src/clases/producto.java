package clases;

public class producto {

	int id_producto;
	String dispositivo_de_entrega_producto;
	String marca_producto;
	String capacidad_produto;
	String color_producto;
	Double precio_producto;
	String direccion_foto_producto;

	public int getId_producto() {
		return id_producto;
	}

	public void setId_producto(int id_producto) {
		this.id_producto = id_producto;
	}

	public String getDispositivo_de_entrega_producto() {
		return dispositivo_de_entrega_producto;
	}

	public void setDispositivo_de_entrega_producto(String dispositivo_de_entrega_producto) {
		this.dispositivo_de_entrega_producto = dispositivo_de_entrega_producto;
	}

	public String getMarca_producto() {
		return marca_producto;
	}

	public void setMarca_producto(String marca_producto) {
		this.marca_producto = marca_producto;
	}

	public String getCapacidad_produto() {
		return capacidad_produto;
	}

	public void setCapacidad_produto(String capacidad_produto) {
		this.capacidad_produto = capacidad_produto;
	}

	public String getColor_producto() {
		return color_producto;
	}

	public void setColor_producto(String color_producto) {
		this.color_producto = color_producto;
	}

	public Double getPrecio_producto() {
		return precio_producto;
	}

	public void setPrecio_producto(Double precio_producto) {
		this.precio_producto = precio_producto;
	}

	public String getDireccion_foto_producto() {
		return direccion_foto_producto;
	}

	public void setDireccion_foto_producto(String direccion_foto_producto) {
		this.direccion_foto_producto = direccion_foto_producto;
	}

}
